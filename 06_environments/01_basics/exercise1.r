### List three ways in which an environment differs from a list.

# 1.) Every element in an environment has a unique name, this is not true with lists.
# 2.) Lists, unlike environments, can be without names.
# 3.) The order of a list matters, unlike that of an environment.
# 4.) It can't be compared with `==`, only `indentical` (though realistically, lists shouldn't be compared with `==` either)
# 5.) Environments have parents, lists do not. (So sad.)
# 6.) Environments have reference semantics, lists do not.
